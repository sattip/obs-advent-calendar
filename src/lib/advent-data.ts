export function getDayByNumber(day: number): DayData | undefined {
  return adventDays.find(d => d.day === day);
}

export function getUnlockedDays(): DayData[] {
  const today = new Date();
  const currentDay = today.getDate();
  const currentMonth = today.getMonth() + 1;

  if (currentMonth !== 12) return [];

  return adventDays.filter(d => d.day <= currentDay);
}

export function isDayUnlocked(day: number): boolean {
  const today = new Date();
  const currentDay = today.getDate();
  const currentMonth = today.getMonth() + 1;

  if (currentMonth !== 12) return false;
  return day <= currentDay;
}

export function getCurrentDay(): number {
  const today = new Date();
  const currentMonth = today.getMonth() + 1;

  if (currentMonth !== 12) return 0;
  return Math.min(today.getDate(), 24);
}
